def sort_words_by_length(word_list):
    """
    Сортирует список слов по длине в порядке возрастания.

    Args:
        word_list: Список строк (слов).

    Returns:
        Список строк, отсортированный по длине.
    """
    return sorted(word_list, key=len)  # Используем sorted() и key=len для сортировки по длине

def main():
    """
    Основная функция программы, которая получает список слов, сортирует его и выводит результат.
    """

    # 1. Получение списка слов (выбор пользователя: готовый список или ввод)
    choice = input("Выберите способ ввода слов: (1 - готовый список, 2 - ввод вручную): ")

    if choice == "1":
        # Готовый список слов
        words = ["яблоко", "банан", "апельсин", "киви", "груша", "ананас"]
    elif choice == "2":
        # Ввод слов пользователем
        words = []
        num_words = int(input("Сколько слов вы хотите ввести? "))
        for i in range(num_words):
            word = input(f"Введите слово #{i+1}: ")
            words.append(word)
    else:
        print("Некорректный выбор.  Используется список по умолчанию.")
        words = ["яблоко", "банан", "апельсин"]

    # 2. Сортировка списка слов по длине
    sorted_words = sort_words_by_length(words)

    # 3. Вывод отсортированного списка
    print("\nСлова, отсортированные по длине:")
    for word in sorted_words:
        print(word)


if __name__ == "__main__":
    main()