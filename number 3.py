def найти_наибольшие_и_наименьшие(список_чисел):
  """
  Находит три наименьших и три наибольших числа в списке.

  Args:
    список_чисел: Список целых чисел.

  Returns:
    Кортеж, содержащий два списка: (три_наименьших, три_наибольших)
    Если список содержит менее трех элементов, возвращает доступные наименьшие и наибольшие.
  """
  список_чисел.sort()  # Сортируем список по возрастанию

  три_наименьших = список_чисел[:3]  # Первые три элемента - наименьшие
  три_наибольших = список_чисел[-3:]  # Последние три элемента - наибольшие

  return три_наименьших, три_наибольших


# Получение списка чисел от пользователя
числа_строкой = input("Введите список целых чисел через пробел: ")
числа = [int(x) for x in числа_строкой.split()]

# Альтернативный вариант - заранее заданный список
# числа = [5, 2, 8, 1, 9, 4, 7, 3, 6, 0]


наименьшие, наибольшие = найти_наибольшие_и_наименьшие(числа)

print("Три наименьших числа:", наименьшие)
print("Три наибольших числа:", наибольшие)